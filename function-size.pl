sub show {
    my ($t, $d) = @_;
    my ($med, $avr, $funcs) = funcsize($t, "src lib");
    if($med) {
        printf "$d;%.2f;%.2f;%u\n", $med, $avr, $funcs;
    }

}

foreach my $t (sort sortthem @releases) {
    if(num($t) <= 82000) {
        next;
    }
    my $d = tag2date($t);
    show($t, $d);
}

