}

sub funcsize {
    my ($tag, $path) = @_;

    # Get all source files to check
    my @files;
    open(G, "git ls-tree -r --name-only $tag -- $path 2>/dev/null|");
    while(<G>) {
        chomp;
        if($_ =~ /\.c\z/) {
            # we only check C files
            push @files, $_;
        }
    }
    close(G);

    my $cmd;
    for(@files) {
        $cmd .= "$tag:$_ ";
    }

    #  Modified McCabe Cyclomatic Complexity
    #  |   Traditional McCabe Cyclomatic Complexity
    #  |       |    # Statements in function
    #  |       |        |   First line of function
    #  |       |        |       |   # lines in function
    #  |       |        |       |       |

    my @lin;
    my $funcs;
    open(G, "git show $cmd 2>/dev/null| pmccabe 2>/dev/null|");
    while(<G>) {
        if($_ =~ /^(\d+)\t(\d+)\t(\d+)\t(\d+)\t(\d+)/) {
            my ($modmccabe, $mccabe, $statements, $first, $lines)=
                ($1, $2, $3, $4, $5);
            if($modmccabe > 1000) {
                # pmccabe did not like this
                return 0;
            }
            push @lin, $lines;
            $funcs++;
        }
    }
    close(G);
    return median(@lin), average(@lin), $funcs;
}

