foreach my $t (sort sortthem @releases) {

    if(num($t) < 71008) {
        # no RELEASE-NOTES before 7.10.8
        next;
    }
    if(num($t) <= 82100) {
        # cached
        next;
    }

    my $d = tag2date($t);
    my $c = ncontribs($t);
    if($c) {
        push @pp, $c;
        if(scalar(@pp) > 7) {
            shift @pp;
        }
        # prettyfy
        $t =~ s/_/./g;
        $t =~ s/-/ /g;
        printf "$d;$c;%d\n", median(@pp);
    }
}
