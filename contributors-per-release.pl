#!/usr/bin/perl

require "./stats/median.pm";
require "./stats/tag2date.pm";

sub num {
    my ($t)=@_;
    if($t =~ /^curl-(\d)_(\d+)_(\d+)/) {
        return 10000*$1 + 100*$2 + $3;
    }
    elsif($t =~ /^curl-(\d)_(\d+)/) {
        return 10000*$1 + 100*$2;
    }
}


sub sortthem {
    return num($a) <=> num($b);
}

@alltags= `git tag -l`;

foreach my $t (@alltags) {
    chomp $t;
    if($t =~ /^curl-([0-9_]*[0-9])\z/) {
        push @releases, $t;
    }
}

sub ncontribs {
    my ($tag)=@_;
    open(G, "git show $tag:RELEASE-NOTES|");
    my @rl = <G>;
    close(G);
    my $c = 0;
    for my $r (@rl) {
        if($r =~ /^ *\((\d+) contributors\)/) {
            $c = $1;
            last;
        }
    }

    if(!$c) {
        # no counter, make our own count
        my $o = 0;
        for my $r (@rl) {
            if($r =~ /friends like these/) {
                $o = 1;
            }
            elsif($o == 1) {
                my @n = split(/, /, $r);
                $c += scalar(@n);
            }
            if($r =~ /Thanks!/) {
                last;
            }
        }
    }

    return $c;
}

