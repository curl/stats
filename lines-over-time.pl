sub show {
    my ($t, $d) = @_;
    my ($blanks, $comments, $code) = comments($t, "src lib include");
    my ($lblanks, $lcomments, $lcode) = comments($t, "lib");
    printf "$d;%u;%u;%u\n", $comments +$code, $lcomments + $lcode,
        ($comments +$code) - ($lcomments + $lcode);
}

foreach my $t (sort sortthem @releases) {
    if(num($t) <= 81101) {
        next;
    }
    my $d = tag2date($t);
    show($t, $d);
}

