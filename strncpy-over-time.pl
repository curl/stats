sub show {
    my ($t, $d) = @_;
    my ($cpy, $code) = cpyuse($t, "lib src");
    printf "$d;%.3f;%u\n", $cpy * 1000 / $code, $cpy;
}

foreach my $t (sort sortthem @releases) {
    if(num($t) <= 81001) {
        next;
    }
    my $d = tag2date($t);
    show($t, $d);
}

