sub complexity {
    my ($tag, $path) = @_;

    # Get source files to count
    my @files;
    open(G, "git ls-tree -r --name-only $tag -- $path 2>/dev/null|");
    while(<G>) {
        chomp;
        if($_ =~ /[ch]\z/) {
            push @files, $_;
        }
    }
    close(G);

    my $cmd;
    for(@files) {
        $cmd .= "$tag:$_ ";
    }

    #  Modified McCabe Cyclomatic Complexity
    #  |   Traditional McCabe Cyclomatic Complexity
    #  |       |    # Statements in function
    #  |       |        |   First line of function
    #  |       |        |       |   # lines in function
    #  |       |        |       |       |

    my @score;
    my $funcs;
    my $totlines;

    open(G, "git show $cmd 2>/dev/null| pmccabe 2>/dev/null|");
    while(<G>) {
        if($_ =~ /^(\d+)\t(\d+)\t(\d+)\t(\d+)\t(\d+)/) {
            my ($modmccabe, $mccabe, $statements, $first, $lines)=($1, $2, $3, $4, $5);
            if($modmccabe > 1000) {
                # something is wrong
                #print STDERR $_;
                return 0;
            }

            my $step;
            if($modmccabe <= 10) {
                $step = 0;
            }
            elsif($modmccabe <= 30) {
                $step = 1;
            }
            elsif($modmccabe <= 50) {
                $step = 2;
            }
            elsif($modmccabe <= 60) {
                $step = 3;
            }
            elsif($modmccabe <= 70) {
                $step = 4;
            }
            elsif($modmccabe <= 80) {
                $step = 5;
            }
            elsif($modmccabe <= 90) {
                $step = 6;
            }
            elsif($modmccabe <= 100) {
                $step = 7;
            }
            elsif($modmccabe <= 150) {
                $step = 8;
            }
            elsif($modmccabe <= 200) {
                $step = 9;
            }
            else {
                $step = 10;
            }
            $score[$step] += $lines;
            $totlines += $lines;
        }
    }
    close(G);
    my @share;
    my $acc;
    for my $s (0 .. 10) {
        $share[$s] = ($score[$s]*100 / $totlines) + $acc;
        $acc += ($score[$s]*100 / $totlines);
    }
    return @share;
}

