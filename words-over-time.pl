sub show {
    my ($t, $d) = @_;
    my $words = count($t, "src lib include");
    printf "$d;%u\n", $words;
}

foreach my $t (sort sortthem @releases) {
    if(num($t) <= 81900) {
        next;
    }
    my $d = tag2date($t);
    show($t, $d);
}

$t=`git describe --match "curl*"`;
chomp $t;

my ($sec,$min,$hour,$mday,$mon,$year,$wday,$yday,$isdst) =
    localtime(time);
my $d = sprintf "%04d-%02d-%02d", $year + 1900, $mon + 1, $mday;

show($t, $d);
